package kikakuya.model;

public class Lists {
	
	private int listId;
	private String listTitle;
	
	public Lists() {
		
	}

	public int getListId() {
		return listId;
	}

	public void setListId(int listId) {
		this.listId = listId;
	}

	public String getListTitle() {
		return listTitle;
	}

	public void setListTitle(String listTitle) {
		this.listTitle = listTitle;
	}
	
	
	
}
