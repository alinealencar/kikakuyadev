package kikakuya.model;

public class GuestPlusOne {

	private String fullName, mealChoice;
	private int guestGuestId, isAdult;
	
	public GuestPlusOne() {
		super();
	}

	public String getMealChoice() {
		return mealChoice;
	}

	public void setMealChoice(String mealChoice) {
		this.mealChoice = mealChoice;
	}

	public int getGuestGuestId() {
		return guestGuestId;
	}

	public void setGuestGuestId(int guestGuestId) {
		this.guestGuestId = guestGuestId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public int getIsAdult() {
		return isAdult;
	}

	public void setIsAdult(int isAdult) {
		this.isAdult = isAdult;
	}
	 
	
	
}
