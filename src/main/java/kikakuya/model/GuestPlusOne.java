package kikakuya.model;

public class GuestPlusOne {

	private String firstName, lastName, fullName, mealChoice;
	private int guestGuestId;
	
	public GuestPlusOne() {
		super();
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getMealChoice() {
		return mealChoice;
	}

	public void setMealChoice(String mealChoice) {
		this.mealChoice = mealChoice;
	}

	public int getGuestGuestId() {
		return guestGuestId;
	}

	public void setGuestGuestId(int guestGuestId) {
		this.guestGuestId = guestGuestId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	 
	
	
}
